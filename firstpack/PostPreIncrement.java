package firstpack;

public class PostPreIncrement {

	public static void main(String[] args) {
		int i=5;
		int j=++i;//pre increment
		System.out.println(i);
		System.out.println(j);
		
		int x=5;
		int y=x++;//post increment
		System.out.println(x);
		System.out.println(y);
		
	}

}
