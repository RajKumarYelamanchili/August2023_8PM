package firstpack;

public class RelationalOperators {

	public static void main(String[] args) {
		int a=20,b=10;
		
		System.out.println("a<b --->" + (a<b));//false
	
		System.out.println("a>b --->" + (a>b));//true
		
		System.out.println("a<=b --->" + (a<=b));//false
		
		System.out.println("a>=b --->" + (a>=b));//true
		
		System.out.println("a==b --->" + (a==b));//false
		
		System.out.println("a!=b --->" + (a!=b));//true

	}

}
